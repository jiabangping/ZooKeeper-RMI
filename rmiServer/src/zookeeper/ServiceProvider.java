package zookeeper;

import java.rmi.Naming;
import java.rmi.Remote;
import java.rmi.registry.LocateRegistry;
import java.util.concurrent.CountDownLatch;

import org.apache.zookeeper.CreateMode;
import org.apache.zookeeper.WatchedEvent;
import org.apache.zookeeper.Watcher;
import org.apache.zookeeper.ZooDefs;
import org.apache.zookeeper.ZooKeeper;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class ServiceProvider {
	private static final Logger LOGGER = LoggerFactory.getLogger(ServiceProvider.class);
	//用於等待 SyncConnected 事件触发后继续执行 当前线程
	private  CountDownLatch latch = new CountDownLatch(1);
	
	public void publish(Remote remote ,String host,int port) {
		String url = publishService(remote,host,port);//发布RMI服务并返回 RMI 地址
		if(url != null) {
			ZooKeeper zk = connectServer();//连接 ZooKeeper 服务器并获取Zookeeper 对象
			if(zk != null) {
				createNode(zk,url);//创建 ZNode 并将 RMI 地址放在 ZNode上
			}
		}
	}
	
	//发布 RMI服务
	private String publishService(Remote remote,String host,int port) {
		String url = null;
		try {
			url = String.format("rmi://%s:%d/%s", host,port,remote.getClass().getName());
			LocateRegistry.createRegistry(port);
			Naming.rebind(url, remote);
			LOGGER.debug("publish rmi service url:"+url);
		} catch (Exception e) {
			LOGGER.error("",e);
		}	
		return url;
	}
	
	
	//连接 Zookeeper 服务器
	private ZooKeeper connectServer() {
		ZooKeeper zk = null;
		try {
			zk = new ZooKeeper(Constant.ZK_CONNECTION_STRING, Constant.ZK_SESSION_TIMEOUT, new Watcher() {
				
				@Override
				public void process(WatchedEvent event) {
					System.out.println("ZooKeeper WatchEvent: process()被调用"+event);
					if(event.getState() == Event.KeeperState.SyncConnected) {
						latch.countDown();//唤醒当前正在执行的线程
					}
				}
			});
			latch.await();//使当前线程处于等待状态
		} catch (Exception e) {
			 LOGGER.error("", e);
		}
		return zk;
	}
	
	
	//创建 Znode
	private void createNode(ZooKeeper zk,String url) {
		try {
			byte[] data = url.getBytes();
			String path = zk.create(Constant.ZK_PROVIDER_PATH,data,ZooDefs.Ids.OPEN_ACL_UNSAFE,CreateMode.EPHEMERAL_SEQUENTIAL);//// 创建一个临时性且有序的 ZNode
			 LOGGER.debug("create zookeeper node ({} => {})", path, url);
		} catch (Exception e) {
			LOGGER.error("", e);
		}
	}
	
	
}
